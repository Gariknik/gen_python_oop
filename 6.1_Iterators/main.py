class Peekable:
    def __init__(self, iterable):
        self.iterable = list(iterable)
        self.it = iter(iterable)
        self.index = 0

    def __iter__(self):
        return self
    
    def __next__(self):
        self.index += 1
        return next(self.it)
    
    def peek(self, default=None):
        try:
            elem = self.iterable[self.index]
            return elem
        except:
            if default == None:
                raise StopIteration
            else:
                return default
