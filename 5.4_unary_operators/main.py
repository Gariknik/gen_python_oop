class Matrix:
    def __init__(self, rows, cols, value=0, matrix=None):
        if matrix is None:
            self.rows = rows
            self.cols = cols
            self.value = value
            self.matrix = [[value for _ in range(cols)] for _ in range(rows)]
        else:
            self.rows = rows
            self.cols = cols
            self.matrix = matrix



    def get_value(self, i_row, i_col):
        return self.matrix[i_row][i_col]

    def set_value(self, i_row, i_col, value):
        self.matrix[i_row][i_col] = value

    def __repr__(self):
        return f'Matrix({self.rows}, {self.cols})'

    def __str__(self):
        return '\n'.join([' '.join([str(col) for col in row]) for row in self.matrix])

    def __pos__(self):
        return Matrix(self.rows, self.cols, self.value, self.matrix)

    def __neg__(self):
        return Matrix(self.rows, self.cols, -1*self.value, [[-1 * col for col in row] for row in self.matrix])

    def __invert__(self):
        return Matrix(self.cols, self.rows, self.value, [list(i) for i in zip(*self.matrix)])

    def __round__(self, n=None):
        if n is None:
            return Matrix(self.cols, self.rows, self.value, self.matrix)
        return Matrix(self.cols, self.rows, self.value, [[round(col, n) for col in row] for row in self.matrix])






